public class Main {
    public static void main(String[] args) {
        for (int i = 5; i > 0; i--)
            System.out.println("Hello World" + " " + i);
        
        int i = 0;
        while (i < 10) {
            System.out.println("Hello World with While Loop " + i);
            i += 1;
        }
    }
}
